// Every palette key across every variant, computed once so we can clean-reset
// the document before applying a new palette.
const ALL_KEYS: ReadonlySet<string> = (() => {
  const s = new Set<string>();
  for (const p of Object.values(PALETTES)) {
    for (const k of Object.keys(p.light)) s.add(k);
    for (const k of Object.keys(p.dark)) s.add(k);
  }
  return s;
})();

export function applyPaletteToDom(
  paletteName: string,
  mode: 'light' | 'dark',
) {
  const palette = PALETTES[paletteName] ?? PALETTES['rose-pine'];
  const vars = palette[mode];
  const style = document.documentElement.style;
  for (const k of ALL_KEYS) style.removeProperty(k);
  for (const [k, v] of Object.entries(vars)) style.setProperty(k, v);
}
